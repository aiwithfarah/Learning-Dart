// Define a function named showPrice that takes one parameter: a double named price.
// Inside the function, print: "The current price is: $price".
// Inside main(), call this function twice:
// Once with the value 10.50
// Once with the value 99.99

void showPrice(double price) {
  print('The current price is: $price');
}

void main() {
  showPrice(10.50);
  showPrice(99.99);
}
